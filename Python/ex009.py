"""
Elabore um programa que calcule e apresente o valor do volume de uma caixa
retangular, utilizando a fórmula VOLUME = COMPRIMENTO*LARGURA*ALTURA.
===============================================================================
Entendimento
1.Fazer a leitura de COMPRIMENTO,LARGURA,ALTURA em números reais
2.Calcular VOLUME conforme a fórmula
3.Escreva VOLUME
"""
COMPRIMENTO = float(input('Digite COMPRIMENTO: '))
LARGURA = float(input('Digite LARGURA: '))
ALTURA = float(input('Digite ALTURA: '))
VOLUME = COMPRIMENTO*LARGURA*ALTURA
print(VOLUME)


